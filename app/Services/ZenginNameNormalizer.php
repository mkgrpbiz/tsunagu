<?php

namespace App\Services;

class ZenginNameNormalizer
{
    private const KANA_MAP = [
        'ア' => 'ｱ', 'イ' => 'ｲ', 'ウ' => 'ｳ', 'エ' => 'ｴ', 'オ' => 'ｵ',
        'カ' => 'ｶ', 'キ' => 'ｷ', 'ク' => 'ｸ', 'ケ' => 'ｹ', 'コ' => 'ｺ',
        'サ' => 'ｻ', 'シ' => 'ｼ', 'ス' => 'ｽ', 'セ' => 'ｾ', 'ソ' => 'ｿ',
        'タ' => 'ﾀ', 'チ' => 'ﾁ', 'ツ' => 'ﾂ', 'テ' => 'ﾃ', 'ト' => 'ﾄ',
        'ナ' => 'ﾅ', 'ニ' => 'ﾆ', 'ヌ' => 'ﾇ', 'ネ' => 'ﾈ', 'ノ' => 'ﾉ',
        'ハ' => 'ﾊ', 'ヒ' => 'ﾋ', 'フ' => 'ﾌ', 'ヘ' => 'ﾍ', 'ホ' => 'ﾎ',
        'マ' => 'ﾏ', 'ミ' => 'ﾐ', 'ム' => 'ﾑ', 'メ' => 'ﾒ', 'モ' => 'ﾓ',
        'ヤ' => 'ﾔ', 'ユ' => 'ﾕ', 'ヨ' => 'ﾖ',
        'ラ' => 'ﾗ', 'リ' => 'ﾘ', 'ル' => 'ﾙ', 'レ' => 'ﾚ', 'ロ' => 'ﾛ',
        'ワ' => 'ﾜ', 'ヲ' => 'ｦ', 'ン' => 'ﾝ',
        'ァ' => 'ｧ', 'ィ' => 'ｨ', 'ゥ' => 'ｩ', 'ェ' => 'ｪ', 'ォ' => 'ｫ',
        'ャ' => 'ｬ', 'ュ' => 'ｭ', 'ョ' => 'ｮ', 'ッ' => 'ｯ',
        'ヮ' => 'ﾜ', 'ヵ' => 'ｶ', 'ヶ' => 'ｹ',
        'ガ' => 'ｶﾞ', 'ギ' => 'ｷﾞ', 'グ' => 'ｸﾞ', 'ゲ' => 'ｹﾞ', 'ゴ' => 'ｺﾞ',
        'ザ' => 'ｻﾞ', 'ジ' => 'ｼﾞ', 'ズ' => 'ｽﾞ', 'ゼ' => 'ｾﾞ', 'ゾ' => 'ｿﾞ',
        'ダ' => 'ﾀﾞ', 'ヂ' => 'ﾁﾞ', 'ヅ' => 'ﾂﾞ', 'デ' => 'ﾃﾞ', 'ド' => 'ﾄﾞ',
        'バ' => 'ﾊﾞ', 'ビ' => 'ﾋﾞ', 'ブ' => 'ﾌﾞ', 'ベ' => 'ﾍﾞ', 'ボ' => 'ﾎﾞ',
        'パ' => 'ﾊﾟ', 'ピ' => 'ﾋﾟ', 'プ' => 'ﾌﾟ', 'ペ' => 'ﾍﾟ', 'ポ' => 'ﾎﾟ',
        'ヴ' => 'ｳﾞ',
        'ヰ' => 'ｲ', 'ヱ' => 'ｴ',
        '。' => '.', '、' => ',', '・' => '･', 'ー' => 'ｰ', '「' => '', '」' => '',
    ];

    /**
     * BIMONIの全銀CSV出力(GAS)で使われている受取人名の正規化ロジックをそのまま移植したもの。
     * 全角英数字・全角カタカナを半角に寄せ、銀行が受け付けやすい文字だけを残す。
     */
    public static function normalize(string $value): string
    {
        $s = trim($value);
        $s = preg_replace('/[ 　]/u', '', $s);

        $s = preg_replace_callback('/[Ａ-Ｚａ-ｚ０-９]/u', function (array $m) {
            return mb_chr(mb_ord($m[0], 'UTF-8') - 0xFEE0, 'UTF-8');
        }, $s);

        $symbolMap = [
            "\u{2010}" => 'ｰ', "\u{2011}" => 'ｰ', "\u{2012}" => 'ｰ',
            "\u{2013}" => 'ｰ', "\u{2014}" => 'ｰ', "\u{2015}" => 'ｰ',
            '－' => 'ｰ', "\u{2212}" => 'ｰ',
            '（' => '(', '）' => ')', '．' => '.', '／' => '/',
        ];

        $s = strtr($s, $symbolMap + self::KANA_MAP);

        $s = preg_replace('/[^0-9A-Za-zｦ-ﾟ()\.\/\-]/u', '', $s);

        return $s;
    }
}
